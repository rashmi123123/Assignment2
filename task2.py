# sum_1_to_50.py
# Program to calculate the sum of integers from 1 to 50

# Step 1: Initialize a variable to store the sum
total_sum = 0

# Step 2: Use for loop to iterate from 1 to 50
for num in range(1, 51):
    total_sum += num

# Step 3: Display the final sum
print("The sum of integers from 1 to 50 is:", total_sum)
